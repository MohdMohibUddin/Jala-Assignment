// 3. Program to equal operator and not equal operators
public class equalnotequal {
   
    public static void main(String[] args) {
        int a = 1, b = 8;
        if(a==b){
            System.out.println("a and b are equal");
        }
        else if(a!=b){
            System.out.println("a and b are not equal");
        }
        
    }

}
